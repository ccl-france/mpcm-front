	lieu à des fins d'informations (type newsletter). En aucun cas les données confiées ne seront vendues
	ou partagées à des tiers pour d'autre objectifs que les deux susmentionnés.
	<br /><br />
	Les données seront conservées aussi longtemps que le collectif existe et communique par newsletter
	en utilisant ces données.

